def เรียงลำดับ(ข้อมูล, *, key=None, reverse=False):
    ข้อมูล.sort(key=key, reverse=reverse)
    return ข้อมูล


# List helpers
def เพิ่ม(รายการ, สมาชิก):
    """เพิ่มสมาชิกเข้าท้ายรายการ"""
    รายการ.append(สมาชิก)
    return รายการ


def ลบ(รายการ, สมาชิก):
    """ลบสมาชิกแรกจากรายการ"""
    รายการ.remove(สมาชิก)
    return รายการ


def แทรก(รายการ, ตำแหน่ง, สมาชิก):
    """แทรกสมาชิกที่ตำแหน่งที่ระบุ"""
    รายการ.insert(ตำแหน่ง, สมาชิก)
    return รายการ


def นับ(รายการ, สมาชิก):
    """นับจำนวนครั้งที่สมาชิกปรากฏในรายการ"""
    return รายการ.count(สมาชิก)


def หาตำแหน่ง(รายการ, สมาชิก):
    """หาตำแหน่งแรกของสมาชิกในรายการ"""
    return รายการ.index(สมาชิก)


# String helpers
def ตัดช่องว่าง(ข้อความ):
    """ตัดช่องว่างที่ต้นและท้ายข้อความ"""
    return ข้อความ.strip()


def แยกข้อความ(ข้อความ, ตัวแบ่ง=None):
    """แยกข้อความตามตัวแบ่ง (default: ช่องว่าง)"""
    return ข้อความ.split(ตัวแบ่ง)


def รวมข้อความ(ตัวเชื่อม, รายการข้อความ):
    """รวมรายการข้อความด้วยตัวเชื่อม"""
    return ตัวเชื่อม.join(รายการข้อความ)


# Dictionary helpers
def ดึงค่า(พจนานุกรม, กุญแจ, ค่าเริ่มต้น=None):
    """ดึงค่าจากพจนานุกรม หากไม่พบให้คืนค่าเริ่มต้น"""
    return พจนานุกรม.get(กุญแจ, ค่าเริ่มต้น)


